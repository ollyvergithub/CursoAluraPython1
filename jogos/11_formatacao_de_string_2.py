print("Valor R$ {:f}".format(1.59))
print("Valor R$ {:.2f}".format(1.59))
print("Valor R$ {:.2f}".format(1.5))
print("Valor R$ {:9.2f}".format(1234.50))
print("Valor R$ {:7.2f}".format(4.50))
print("Valor R$ {:07.2f}".format(4.50))
print("Valor R$ {:07d}".format(4))
print("Data {:02d}/{:02d}".format(9,2))
print("Data {:02d}/{:02d}/{:4d}".format(9,2,1972))

